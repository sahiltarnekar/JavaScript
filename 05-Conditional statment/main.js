// Conditional Statements
// if condition
let a = 100;
let b = 100;
if (a == b) {
    document.write("a is equal to b");
    document.write("<br>");
}

// if else condition
let c = 100;
let d = 200;
if (c == d) {
    document.write("a is equal to b");
    document.write("<br>");
} else {
    document.write("a is not equal to b");
    document.write("<br>");
}
// if else if condition
let e = 100;
let f = 200;
if (e == f) {
    document.write("a is equal to b");
    document.write("<br>");
} else if (e > f) {
    document.write("a is greater than b");
    document.write("<br>");
} else {
    document.write("a is less than b");
    document.write("<br>");
}       
// Switch case
let g = 3;
switch (g) {
    case 1:
        document.write("a is equal to 1");
        document.write("<br>");
        break;
    case 2:
        document.write("a is equal to 2");
        document.write("<br>");
        break;
    case 3:
        document.write("a is equal to 3");
        document.write("<br>");
        break;
    default:    
        document.write("a is not equal to 1, 2 or 3");
        document.write("<br>");
        break;
}

