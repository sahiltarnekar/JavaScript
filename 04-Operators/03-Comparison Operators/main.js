// Comparison Operators
// == equal to
let a = 100;
let b = 100;
document.write(a == b);
document.write("<br>");
// === strict equal to
document.write(a === b);
document.write("<br>");
// != not equal to
document.write(a != b);
document.write("<br>");
// !== strict not equal to
document.write(a !== b);
document.write("<br>");
// > greater than
document.write(a > b);
document.write("<br>");
// < less than
document.write(a < b);
document.write("<br>");
// >= greater than or equal to
document.write(a >= b);
document.write("<br>");
// <= less than or equal to
document.write(a <= b);
document.write("<br>");

